fn main(){
    input!(S:chars,T:usize);
    let S:Vec<char> = S;
    let mut L = S.iter().fold(0,|sum,&c| sum + (c=='L') as i32);
    let mut R = S.iter().fold(0,|sum,&c| sum + (c=='R') as i32);
    let mut U = S.iter().fold(0,|sum,&c| sum + (c=='U') as i32);
    let mut D = S.iter().fold(0,|sum,&c| sum + (c=='D') as i32);
    let mut X = S.iter().fold(0,|sum,&c| sum + (c=='?') as i32);
    let LR:i32 = (L-R).abs();
    let UD:i32 = (U-D).abs();
    if T==1{
        println!("{}",LR+UD+X);
    }else{
        let Y = X - LR-UD.abs();
        if Y >= 0{
            println!("{}",Y%2 as i32);
        }else{
            println!("{}",LR.abs()+UD.abs()-X);
        }
    }
}