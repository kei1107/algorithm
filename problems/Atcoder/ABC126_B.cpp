template<class Type>
Type solve(Type res = Type()){
    ll S; cin >> S;
    ll L = S/100;
    ll R = S%100;
    bool f1 = false,f2 = false;
    if(L <= 99 && 0 < R && R <= 12) f1 = true;
    if(0 < L && L <= 12 && R <= 99) f2 = true;
    if(f1 && f2) return "AMBIGUOUS";
    if(f1 && !f2) return "YYMM";
    if(!f1 && f2) return "MMYY";
    return "NA";
}
