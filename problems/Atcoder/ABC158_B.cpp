/*
 <url:https://atcoder.jp/contests/abc158/tasks/abc158_b>
 問題文============================================================
 B - Count Balls
 =================================================================
 解説=============================================================

 まず、(A+B)個周期で青A個・赤B個のボール列が並ぶこととなる。
 よって(A+B)周期がNまでにいくつあるか計算し、その周期数*Aとすることで
 周期上での青いボールの個数を計算できる。
 また、Nを(A+B)で割ったあまりを出すことで、残りの数えきれていない青+赤ボールの個数が得られるので。その中には最大A個まで青ボールが含まれることになる。

 https://atcoder.jp/contests/abc158/submissions/11617822
 ================================================================
 */

template<class Type>
Type solve(Type res = Type()){
    ll N,A,B; cin >> N >> A >> B;

    res += N/(A+B) * A;
    N %= (A+B);
    res += min(N,A);
    return res;
}
