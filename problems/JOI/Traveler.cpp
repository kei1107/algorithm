const int Mod = (int)1e5;
int main(void) {
	cin.tie(0); ios::sync_with_stdio(false);
	int n, m; cin >> n >> m;
	vector<int> cusum(n + 1, 0);
	for (int i = 2; i <= n;i++) {
		cin >> cusum[i];
		cusum[i] += cusum[i - 1];
	}
	int res = 0;
	int now = 1;
	for (int i = 0; i < m;i++) {
		int a; cin >> a;
		if (a > 0) {
			res += cusum[now + a] - cusum[now];
			now = now + a;
		}
		else {
			res += cusum[now] - cusum[now + a];
			now = now + a;
		}
		res %= Mod;
	}
	cout << res << endl;
	return 0;
}