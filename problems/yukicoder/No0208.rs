/*
 <url:https://yukicoder.me/problems/no/208>
 問題文============================================================
 =================================================================
 解説=============================================================
 歩兵がいないとき、対象地点にはmax(x,y)で到達できる
 歩兵が邪魔となるのは
 x==yかつx2==y2かつx2<xのとき
 この場合は1手余分な動作が必要になる。
 ================================================================
 */
fn main(){
    input!(x:usize,y:usize,x2:usize,y2:usize);
    let cost:usize = if x==y&&x2==y2&&x2<x{
        1
    }else {
        0
    };
    println!("{}",max(x,y)+cost);
}