/*
 <url:https://yukicoder.me/problems/no/268>
 問題文============================================================
 =================================================================
 解説=============================================================
 一番大きい周は一番小さいリボンの本数とペア
 ================================================================
 */
fn main(){
    input!(L:[i32;3],C:[i32;3]);
    let mut C:Vec<i32> = C;
    let mut T:Vec<i32> = Vec::new();
    T.push(2*(L[0]+L[1]));
    T.push(2*(L[1]+L[2]));
    T.push(2*(L[2]+L[0]));
    C.sort(); T.sort(); T.reverse();

    let mut ans = 0;
    for i in 0..3{
        ans += T[i]*C[i];
    }
    println!("{}",ans);
}